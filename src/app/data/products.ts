const allCategoryKeys = Object.keys(categoryImages) as (keyof typeof categoryImages)[];
const countriesList = Array.from(new Set(baseProducts.map(p => p.country)));
let nextId = Math.max(...baseProducts.map(p => p.id)) + 1;

const extraProducts: Product[] = [];
countriesList.forEach((country, cIdx) => {
  const flag = baseProducts.find(p => p.country === country)?.flag ?? '';
  for (let i = 1; i <= 10; i++) {
    const category = allCategoryKeys[(i + cIdx) % allCategoryKeys.length];
    const pool = categoryImages[category];
    const namePool = genNames[category] ?? [];
    const descPool = genDescs[category] ?? ['A quality product.'];
    extraProducts.push({
      id: nextId++,
      name: namePool[(i + cIdx * 2) % namePool.length] ?? category,
      price: Number((10 + cIdx * 5 + i * 3).toFixed(2)),
      image: pool[(i + cIdx) % pool.length],
      description: descPool[(i + cIdx) % descPool.length],
      category,
      rating: Number((4 + ((i % 5) * 0.1)).toFixed(1)),
      reviews: i * 12 + cIdx * 7,
      country,
      flag,
    });
  }
});

export const products: Product[] = [...baseProducts, ...extraProducts];
